<h4>1- Dental Fillings:</h4>

Tooth decay, tooth wear and trauma are major causes of damage to a tooth. Dental fillings are used to repair damage to the structure of a tooth or teeth. After the removal of a problematic tooth structure, the tooth is restored with one of several filling materials: Amalgam and composite resin (white filling material) are the commonly used filling materials. Your dentist will advise you on which material to use and its advantages. <br>

<h4>2- Bonding:</h4>

Chipped or cracked teeth are typically repaired through bonding. An enamel-like composite material is applied to the surface of a tooth, sculpted into shape, contoured and polished, making the crack or chip invisible. <br>

<h4>3- Orthodontics: </h4>

Orthodontic treatment can be used to straighten crooked teeth and malocclusions which involved the teeth and jaws. Orthodontics is both a functional and cosmetic treatment, and has become an increasingly popular field of dentistry. <br>

<h4>
4- Root Canal Therapy: 
</h4> 
Root canal therapy removes infected pulp tissue within the root chamber of the tooth. The root of the tooth is filled with an antibacterial filling, and a crown is placed over the tooth to protect it.<br>

<h4>5- Dental Crowns: </h4>

Dental crowns provide an aesthetic and protective solution to teeth that have undergone root canal therapy and that are extensively restored due to decay or trauma. Dental crowns can be made of metal or metal fused to porcelain. Today zirconium crowns are being used for their strength and excellent aesthetics.<br>

